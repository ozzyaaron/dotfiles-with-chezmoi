      lspconfig.cssls.setup {
        capabilities = capabilities,
        on_attach = on_attach,
        settings = {
          css = {
            validate = true,
            lint = {
                unknownAtRules = "ignore" -- Ignore unknown CSS rules (useful for Tailwind, etc.)
            }
